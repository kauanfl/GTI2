    private void jb_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_CalcularActionPerformed
        //Entrada de dados
        ret1.setLadoA( Double.parseDouble( jtf_ladoA.getText()));
         ret1.setLadoB( Double.parseDouble( jtf_ladoB.getText()));
    
        //Processamento
        ret1.calcularPerimetro();
        ret1.calcularArea();
        //Saida de dados
        jtf_Perimetro.setText(
        String.valueOf(ret1.getPerimetro()));
        jtf_Area.setText(
        String.valueOf(ret1.getArea()));
    }//GEN-LAST:event_jb_CalcularActionPerformed

